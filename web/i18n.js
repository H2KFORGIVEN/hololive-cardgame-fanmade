const SUPPORTED_LANGS = ['zh-TW', 'en', 'ja', 'fr'];
const FALLBACK_LANG = 'en';

let currentLang = FALLBACK_LANG;

function _detectBrowserLang() {
  const langs = navigator.languages || [navigator.language || ''];
  for (const tag of langs) {
    const norm = tag.trim();
    if (SUPPORTED_LANGS.includes(norm)) return norm;
    const base = norm.split('-')[0];
    if (base === 'zh') return 'zh-TW';
    const match = SUPPORTED_LANGS.find(s => s.startsWith(base));
    if (match) return match;
  }
  return FALLBACK_LANG;
}

export function initI18n() {
  const saved = localStorage.getItem('holo-card-lang');
  if (saved && SUPPORTED_LANGS.includes(saved)) {
    currentLang = saved;
  } else {
    currentLang = _detectBrowserLang();
  }
  document.documentElement.lang = currentLang;
  return currentLang;
}

export function setLang(lang) {
  if (!SUPPORTED_LANGS.includes(lang)) return;
  currentLang = lang;
  localStorage.setItem('holo-card-lang', lang);
  document.documentElement.lang = lang;
}

export function getLang() {
  return currentLang;
}

export function t(key, params) {
  let str = TRANSLATIONS[currentLang]?.[key] ?? TRANSLATIONS[FALLBACK_LANG]?.[key] ?? key;
  if (params) {
    for (const [k, v] of Object.entries(params)) {
      str = str.replace(`{${k}}`, v);
    }
  }
  return str;
}

export function getSupportedLangs() {
  return SUPPORTED_LANGS.map(code => ({ code, label: TRANSLATIONS[code].lang_label }));
}

export function localized(value, fallback) {
  if (value == null) return fallback ?? '';
  if (typeof value === 'string') return value;
  if (typeof value === 'object' && !Array.isArray(value)) {
    return value[currentLang] ?? value['ja'] ?? value[Object.keys(value)[0]] ?? fallback ?? '';
  }
  return value;
}

export function applyStaticTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
}
